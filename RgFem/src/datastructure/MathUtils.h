#pragma onec
// useful constants for trig
#ifndef PI
#define PI 3.141592653589793
#endif

#ifndef RAD2DEG
#define RAD2DEG (180.0 / PI)
#endif

#ifndef DEG2RAD
#define DEG2RAD (PI / 180.0)
#endif

#ifndef MAX
#define MAX(a, b) ((a) > (b) ? (a) : (b))
#endif

#ifndef MIN
#define MIN(a, b) ((a) < (b) ? (a) : (b))
#endif

